
/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub  - substract two integers
 * @a: first int
 * @b: second int
 * Return: substracted final result
 */
int sub(int a, int b)
{
	return (a - b);
}


/**
 * div - divide two integers
 * @a: first int
 * @b: second int
 * Return: division result
 */
int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - mod two integers
 * @a: first int
 * @b: second int
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}
